# Basic Data structure 
# Lists: Ordered, mutable sequences.
my_list = [1,2,3,4,5]
# Tuples: Ordered, immutable sequences
my_tuple= (1,2,3,4,5)
# Dictionaries: Key-value pairs, mutable.
